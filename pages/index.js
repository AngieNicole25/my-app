import { useEffect, useState } from 'react'
import Head from 'next/head'
import { Inter } from 'next/font/google'
import styles from '@/styles/Home.module.css'
import NavBar from '@/components/NavBar'
import Section1 from '@/components/Section-1'
import Cards from '@/components/Section-2'
import Galeria from '@/components/Section-3'
import Footer from '@/components/Footer'
import cardList from '../public/lista.json'


const inter = Inter({ subsets: ['latin'] })

export default function Home() {
  const [favoritos, setFavoritos] = useState(0)
  useEffect(() => {
    alert("Elementos agregados: " + favoritos)
  }, [favoritos])

  function changeElements(toDo) {
    toDo 
    ? setFavoritos(prev => prev + 1)
    : setFavoritos(prev => prev - 1)
  }
  return (
    <>
      <Head>
        <title>My-App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={styles.main}>
       <NavBar/>
       <Section1/>
       <h4>favoritos {favoritos}</h4>
       {
        cardList.map(card => {
          return <Cards titulo={card.title} img={card.imageURL} cambiarFavoritos={changeElements}/>
        })
       }
        <Galeria/>
        <Footer/>
      </main>
    </>
  )

  }
  
